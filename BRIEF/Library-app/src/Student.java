
    public class Student {
        private String name;
        private String address;
        private String id;

        // Constructeur
        public Student(String name, String address, String id) {
            this.name = name;
            this.address = address;
            this.id = id;
        }

        // Getters
        public String getName() {
            return name;
        }

        public String getAddress() {
            return address;
        }

        public String getId() {
            return id;
        }

    }



