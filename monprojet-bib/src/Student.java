public class Student {
        private String nom;
        private String addresse;
        private String id;

        // Constructeur
        public Student(String nom, String addresse, String id) {
            this.nom = nom;
            this.addresse = addresse;
            this.id = id;
        }

        // Getters
        public String getNom() {
            return nom;
        }

        public String getAddresse() {
            return addresse;
        }

        public String getId() {
            return id;
        }



    }
